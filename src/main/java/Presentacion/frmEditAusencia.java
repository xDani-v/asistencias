    Ausencia crearObjeto(){
          Empleado empleadoSeleccionado = (Empleado) jComboBox1.getSelectedItem();
          int idEmpleado = empleadoSeleccionado.getIdEmpleado();
          Date fechainicio = (Date) jDateChooser1.getDate();
          Date fechaFin = (Date) jDateChooser2.getDate();
          String motivo = jTextArea1.getText();
          
          Ausencia obj=new Ausencia(0, idEmpleado, fechainicio, fechaFin, motivo);
          return obj;
    }
    
     public void cargarDatos(Ausencia ausencia) {
        // Establecer el empleado en el JComboBox
        if (ausencia != null) {
            int idEmpleado = ausencia.getIdEmpleado();
            Empleado empleado = emp.buscarEmpleado(idEmpleado); // MÃ©todo para buscar el empleado por ID
            if (empleado != null) {
                jComboBox1.setSelectedItem(empleado);
            }

            // Establecer las fechas en los JDateChooser
            jDateChooser1.setDate(ausencia.getFechaInicio());
            jDateChooser2.setDate(ausencia.getFechaFin());

            // Establecer el motivo en el JTextArea
            jTextArea1.setText(ausencia.getMotivo());
        }
    }
     
  
