    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() >= 0) {

            int idcod = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            Ausencia emp = olc.buscarAusencia(idcod);
            frmEditAusencia edit=new frmEditAusencia(emp);
            edit.show();
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una ausencia para modificar.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() >= 0) {
            try {
                int res = JOptionPane.showConfirmDialog(null, "Eliminar ausencia", "Desea elimnar de la BD", JOptionPane.YES_NO_OPTION);
                if (res == JOptionPane.YES_OPTION) {
                    int idcod = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
                    olc.eliminarAusencia(idcod);
                    Listar();
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione la fila que desea eliminar");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    
