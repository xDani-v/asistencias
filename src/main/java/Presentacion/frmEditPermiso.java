    Permiso crearObjeto(){
          Empleado empleadoSeleccionado = (Empleado) jComboBox1.getSelectedItem();
          int idEmpleado = empleadoSeleccionado.getIdEmpleado();
          Date fecha_permiso = (Date) jDateChooser1.getDate();
          String tipo = jTextField1.getText();
          String com= jTextArea1.getText();
          Permiso obj=new Permiso(0, idEmpleado, fecha_permiso, tipo, com);
          return obj;
    }
    
     public void cargarDatos(Permiso obj) {
        // Establecer el empleado en el JComboBox
            int idEmpleado = obj.getIdEmpleado();
            Empleado empleado = emp.buscarEmpleado(idEmpleado); // MÃ©todo para buscar el empleado por ID
            if (empleado != null) {
                jComboBox1.setSelectedItem(empleado);
            }

            // Establecer las fechas en los JDateChooser
            jDateChooser1.setDate(obj.getFecha());
            jTextField1.setText(obj.getTipoPermiso());

            // Establecer el motivo en el JTextArea
            jTextArea1.setText(obj.getComentarios());
       
    }
