    Asistencia crearObjeto(){
          Empleado empleadoSeleccionado = (Empleado) jComboBox1.getSelectedItem();
          int idEmpleado = empleadoSeleccionado.getIdEmpleado();
          
          Turno turnoSeleccionado = (Turno) jComboBox2.getSelectedItem();
          int idTurno = turnoSeleccionado.getIdTurno();
          
          Date fechaFin = (Date) jDateChooser1.getDate();
          String comentarios = jTextArea1.getText();
          
            Date dateEntrada = (Date) jSpinner1.getValue();
            Calendar calendarEntrada = Calendar.getInstance();
            calendarEntrada.setTime(dateEntrada);
            int horaInicio = calendarEntrada.get(Calendar.HOUR_OF_DAY);
            int minutoInicio = calendarEntrada.get(Calendar.MINUTE);
            Time horaInicioTime = new Time(horaInicio, minutoInicio, 0);

            // Obt√©n la hora de salida desde el JSpinner
            Date dateSalida = (Date) jSpinner2.getValue();
            Calendar calendarSalida = Calendar.getInstance();
            calendarSalida.setTime(dateSalida);
            int horaFin = calendarSalida.get(Calendar.HOUR_OF_DAY);
            int minutoFin = calendarSalida.get(Calendar.MINUTE);
            Time horaFinTime = new Time(horaFin, minutoFin, 0);
            
            String comentario=jTextArea1.getText();
            
            Asistencia obj=new Asistencia(0,idEmpleado,idTurno,fechaFin,horaInicioTime,horaFinTime,comentario);
            return obj;
    }
    
    public void cargarDatos(Asistencia obj) {
    // Configura el JTextField con el nombre del turno
    jTextArea1.setText(obj.getComentarios());

    int idEmpleado = obj.getIdEmpleado();
    Empleado empleado = emp.buscarEmpleado(idEmpleado);
     jComboBox1.setSelectedItem(empleado);
     
     int idTurno = obj.getIdTurno();
    Turno turno = turn.buscarTurno(idTurno);
     jComboBox2.setSelectedItem(turno);
    // Configura el JSpinner para la hora de entrada
    Time horaInicio = obj.getHoraEntrada();
    Calendar calendarInicio = Calendar.getInstance();
    calendarInicio.setTime(horaInicio);
    jSpinner1.setValue(calendarInicio.getTime());

    // Configura el JSpinner para la hora de salida
    Time horaFin = obj.getHoraSalida();
    Calendar calendarFin = Calendar.getInstance();
    calendarFin.setTime(horaFin);
    jSpinner2.setValue(calendarFin.getTime());
    jDateChooser1.setDate(obj.getFecha());
    
    }
    
     void cargarSpinner() {
    // Configura el modelo SpinnerDateModel con la hora actual
    SpinnerDateModel timeModel = new SpinnerDateModel(new Date(), null, null, Calendar.MINUTE);

    // Configura el editor del JSpinner para usar el formato de hora
    JSpinner.DateEditor timeEditor = new JSpinner.DateEditor(jSpinner1, "HH:mm");

    // Configura el JSpinner existente con el modelo y el editor
    jSpinner1.setModel(timeModel);
    jSpinner1.setEditor(timeEditor);
    }
   
